import sys

class ViewDesk:
    def __init__(self,board):
        self.board=board

#________________________________________________________________________________________________________________
    def show_desk(self):  # вывод поля
        X=self.board.get_vertical_frame()
        func=self.board.get_cell_of_board
        Yabc=self.board.get_horizontal_frame()
        first = "%5s|" % ("")
        for i in Yabc:
            first = first + '%5s|' % (str(i))
        s = first + '\n'
        for x in X:
            s = s + '%5d|' % x
            for y in Yabc:
                s = s + "%5s|" % (func(str(x) + "," + str(y)).visible())# заглушка на todo:пример
            s = s + "\n"

        return s
#______________________________________________________________________________________________________________
